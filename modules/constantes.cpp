#include "constantes.h"
#include <string>

string dirToString(Direccion d) {
    string dir;

    if (d = ARRIBA)
    {
        dir = "Arriba";
    } else if (d = ABAJO)
    {
        dir = "Abajo";
    } else if (d = IZQUIERDA)
    {
        dir = "Izquierda";
    } else if (d = DERECHA)
    {
        dir = "Derecha";
    }
    
    return dir;
}
